str = "Aprender Python é muito fácil"

# seleciona da posição 9 até a 14:
# ‘Python’
print(str[9:15])

# seleciona da posição 9 até o final:
# ‘Python é muito fácil’
print(str[9:])

# seleciona da posição 0 até a 14:
# ‘Aprender Python’
print(str[:15])

# seleciona as posições [0,2,4,6,8,10,12,14]:
# ‘Arne yhn’
print(str[0:15:2])